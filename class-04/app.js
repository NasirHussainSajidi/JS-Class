 //   Pre Increment  ---  Post Increment


 let a = 10
   ++a 
 let b = 5
   b++
 console.log(a)   // 11
 console.log(++a)  // 12
 console.log(b)  //6
 console.log(b++) //6



 //   Pre Decrement  ---  Post Decrement

 let c = 10
 let d = 5
 console.log(c)
 console.log(--c)
 console.log(c--)
 console.log(d)
 console.log(d--)
 console.log(--d)



let x = 10
let f = x++
console.log(f)

let g = ++x
console.log(g)
console.log(f+g)


let y = 5
console.log(++y + y++ + --y + --y + y++ + y-- + ++y)





